package cn.kiway.mdm.utils;

import android.content.ContentValues;
import android.content.Context;
import android.database.Cursor;
import android.database.sqlite.SQLiteDatabase;
import android.database.sqlite.SQLiteOpenHelper;

import java.util.ArrayList;

import cn.kiway.mdm.entity.AppCharge;
import cn.kiway.mdm.entity.Network;
import cn.kiway.mdm.entity.Wifi;


public class MyDBHelper extends SQLiteOpenHelper {
    public static final String DB_NAME = "mdm.db";


    private static final String TABLE_NETWORK = "Network";
    private static final String CREATE_TABLE_NETWORK = " create table  IF NOT EXISTS "
            + TABLE_NETWORK
            + "   (id integer primary key autoincrement,  url  text,  type  text )";

    private static final String TABLE_WIFI = "wifi";
    private static final String CREATE_TABLE_WIFI = " create table  IF NOT EXISTS "
            + TABLE_WIFI
            + "   (id integer primary key autoincrement,  name  text,  password  text  , timeRange text , level text)";

    private static final String TABLE_APP = "app";
    private static final String CREATE_TABLE_APP = " create table  IF NOT EXISTS "
            + TABLE_APP
            + "   (id integer primary key autoincrement,  name  text,  type  text  , timeRange text , version text , packages text , url text)";


    private SQLiteDatabase db;

    public MyDBHelper(Context c) {
        super(c, DB_NAME, null, 3);
    }

    @Override
    public void onUpgrade(SQLiteDatabase db, int arg1, int arg2) {
        db.execSQL("DROP TABLE IF EXISTS " + TABLE_NETWORK);
        db.execSQL(CREATE_TABLE_NETWORK);

        db.execSQL("DROP TABLE IF EXISTS " + TABLE_WIFI);
        db.execSQL(CREATE_TABLE_WIFI);

        db.execSQL("DROP TABLE IF EXISTS " + TABLE_APP);
        db.execSQL(CREATE_TABLE_APP);


    }

    @Override
    public void onCreate(SQLiteDatabase db) {
        this.db = db;
        db.execSQL(CREATE_TABLE_NETWORK);
        db.execSQL(CREATE_TABLE_WIFI);
        db.execSQL(CREATE_TABLE_APP);
    }

    //------------------------------------------Network----------------

    public void addNetwork(Network a) {
        if (db == null)
            db = getWritableDatabase();
        ContentValues values = new ContentValues();
        values.put("url", a.url);
        values.put("type", a.type);
        db.insert(TABLE_NETWORK, null, values);
        db.close();
    }

    public void deleteNetwork() {
        if (db == null)
            db = getWritableDatabase();
        db.delete(TABLE_NETWORK, null, null);
        db.close();
    }

    public ArrayList<Network> getAllNetworks() {
        if (db == null)
            db = getWritableDatabase();
        Cursor cur = db.query(TABLE_NETWORK, null, null, null, null, null, null);
        ArrayList<Network> networks = new ArrayList<>();
        for (cur.moveToFirst(); !cur.isAfterLast(); cur.moveToNext()) {
            String id = cur.getString(cur.getColumnIndex("id"));
            String url = cur.getString(cur.getColumnIndex("url"));
            int type = cur.getInt(cur.getColumnIndex("type"));

            Network a = new Network();
            a.id = id;
            a.url = url;
            a.type = type;
            a.host = Utils.getHostByUrl(url);
            networks.add(a);
        }
        cur.close();
        db.close();
        return networks;
    }

    //------------------------------------------wifi----------------

    public void addWifi(Wifi a) {
        if (db == null)
            db = getWritableDatabase();
        ContentValues values = new ContentValues();
        values.put("name", a.name);
        values.put("password", a.password);
        values.put("timeRange", a.timeRange);
        values.put("level", a.level);
        db.insert(TABLE_WIFI, null, values);
        db.close();
    }

    public void deleteWifi() {
        if (db == null)
            db = getWritableDatabase();
        db.delete(TABLE_WIFI, null, null);
        db.close();
    }

    public ArrayList<Wifi> getAllWifis() {
        if (db == null)
            db = getWritableDatabase();
        Cursor cur = db.query(TABLE_WIFI, null, null, null, null, null, "level desc");
        ArrayList<Wifi> wifis = new ArrayList<>();
        for (cur.moveToFirst(); !cur.isAfterLast(); cur.moveToNext()) {
            String id = cur.getString(cur.getColumnIndex("id"));
            String name = cur.getString(cur.getColumnIndex("name"));
            String password = cur.getString(cur.getColumnIndex("password"));
            String timeRange = cur.getString(cur.getColumnIndex("timeRange"));
            int level = cur.getInt(cur.getColumnIndex("level"));

            Wifi a = new Wifi();
            a.id = id;
            a.name = name;
            a.password = password;
            a.timeRange = timeRange;
            a.level = level;
            wifis.add(a);
        }
        cur.close();
        db.close();
        return wifis;
    }

    //------------------------------------------app----------------

    public void addAppcharge(AppCharge a) {
        if (db == null)
            db = getWritableDatabase();
        ContentValues values = new ContentValues();
        values.put("name", a.name);
        values.put("type", a.type);
        values.put("timeRange", a.timeRange);
        values.put("version", a.version);
        values.put("packages", a.packages);
        values.put("url", a.url);
        db.insert(TABLE_APP, null, values);
        db.close();
    }

    public void deleteAppcharge() {
        if (db == null)
            db = getWritableDatabase();
        db.delete(TABLE_APP, null, null);
        db.close();
    }

    //-1就是全部 0:必选,1:白名单,2:黑名单
    public ArrayList<AppCharge> getAllAppCharges(int type) {
        if (db == null)
            db = getWritableDatabase();
        Cursor cur = null;
        if (type == -1) {
            cur = db.query(TABLE_APP, null, null, null, null, null, null);
        } else {
            cur = db.query(TABLE_APP, null, "type=?", new String[]{type + ""}, null, null, null);
        }
        ArrayList<AppCharge> Appcharges = new ArrayList<>();
        for (cur.moveToFirst(); !cur.isAfterLast(); cur.moveToNext()) {
            String id = cur.getString(cur.getColumnIndex("id"));
            String name = cur.getString(cur.getColumnIndex("name"));
            type = cur.getInt(cur.getColumnIndex("type"));
            String timeRange = cur.getString(cur.getColumnIndex("timeRange"));
            String version = cur.getString(cur.getColumnIndex("version"));
            String packages = cur.getString(cur.getColumnIndex("packages"));
            String url = cur.getString(cur.getColumnIndex("url"));

            AppCharge a = new AppCharge();
            a.id = id;
            a.name = name;
            a.type = type;
            a.timeRange = timeRange;
            a.version = version;
            a.packages = packages;
            a.url = url;
            Appcharges.add(a);
        }
        cur.close();
        db.close();
        return Appcharges;
    }

}